#include <stdio.h>
int main() {
    int distancia;
    int consumo;
    double media;
    printf("digite a distancia percorrida em km: ");
    scanf("%d", &distancia);
    printf("digite o consumo de combustivel em litros: ");
    scanf("%d", &consumo);
    media = (double)distancia / consumo;
    printf("A media de consumo de combustivel e: %.3f km/l\n", media);
    return 0;
}