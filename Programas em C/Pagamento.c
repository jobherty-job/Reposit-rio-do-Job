#include <stdio.h>
#include <string.h>
int main() {
    char nome[20];
    printf("Digite o nome do(a) funcionario(a): ");
    scanf("%19s", nome);
    int horas;
    int ganho;
    int salario;
    printf("Digite o ganho por hora: ");
    scanf("%d", &ganho);
    printf("Digite o numero de horas trabalhadas: ");
    scanf("%d", &horas);
    salario = ganho * horas;
    printf("O salario de %s e: R$ %d\n", nome, salario);
    return 0;
}